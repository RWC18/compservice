<h2>Ի՞նչ է կայքը</h2>

<p>Կայքը կազմակերպության այցեքարտն է. 
Անունը, գնահատականը, որը բարձրանում էր ամիսների, տարիների ընթացքում հիմա այն որոնման հատվածում մի քանի սիմվոլների միջոցով է կատարվում. Եվ այն ինչ տեսնի և լսի Ձեր այցելուն, դրանից է կախված կկպանվի Ձզ հետ, թե ոչ՛.</p>

<p>Տարիներ առաջ մարդիք այս մանն անում էին թղթի վրա՝ այցեքարտերի և բուկլետների տեսքով, ահա թե ինչ են անում նորօրյա տեխնոլոգիաները.</p>

<p>Ձեր մասին կարող են խոսել աշխարհի բոլոր լեզուներով, բոլոր երկրներում. </p>


<h2>Կայքերի հիմնական տեսակները:</h2>

<p><strong>Կայք-այցեքարտ</strong> – Հիմնականում բաղկացած է մի քանի էջից, ունի հեշտ բայց ունիկալ դիզայն.</p>

<p><strong>Պրոմո-կայք</strong> – Բաղկացած է 2-3 էջից, ինչ-որ ապրանքի, ծառայության վերաբերյալ.</p>

<p><strong>Իմիջային կայք</strong> – Պարզ, այցելուին հարմար, գունեղ կայք է. </p>

<p><strong>Ինտերնետ խանութ</strong> – Կայքը ոչ միայն գովազդային է, այլ նաև շահավետ.</p>

<p><strong>Ինտերնետ-պորտալ</strong> – Բարդ բազմահարկանի կայք է, որն հաճախորդին է առաջարկում բազմաթիվ ծառայություններ.</p>

<p><strong>Կայք-ֆորում</strong> Պարզ, բոլորին հասանելի և հասկանալի կայք է առցանց շփման համար.

<p><strong>Բլոգ</strong> – Այն իրենից ներկայացնում է բլոկնոտ, նոթատետր, որը կազմված է նորություններից, հետաքրքրիր փաստերից.</p>


